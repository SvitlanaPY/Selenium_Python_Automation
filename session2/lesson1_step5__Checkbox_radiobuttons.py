from selenium.webdriver.common.by import By
from selenium import webdriver
import time
import math


# –≤—ã—á–∏—Å–ª—è–µ–º 'x'
def calc(value):
    return str(math.log(abs(12 * math.sin(int(value)))))


try:

    # –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –¥—Ä–∞–π–≤–µ—Ä –±—Ä–∞—É–∑–µ—Ä–∞. –ü–æ—Å–ª–µ —ç—Ç–æ–π –∫–æ–º–∞–Ω–¥—ã –≤—ã –¥–æ–ª–∂–Ω—ã —É–≤–∏–¥–µ—Ç—å –Ω–æ–≤–æ–µ –æ—Ç–∫—Ä—ã—Ç–æ–µ –æ–∫–Ω–æ –±—Ä–∞—É–∑–µ—Ä–∞
    # (–æ—Ç–∫—Ä—ã—Ç–∏–µ –±—Ä–∞—É–∑–µ—Ä–∞)
    browser = webdriver.Chrome()

    link = "http://suninjuly.github.io/math.html"
    # –ú–µ—Ç–æ–¥ get() —Å–æ–æ–±—â–∞–µ—Ç –±—Ä–∞—É–∑–µ—Ä—É, —á—Ç–æ –Ω—É–∂–Ω–æ –æ—Ç–∫—Ä—ã—Ç—å —Å–∞–π—Ç –ø–æ —É–∫–∞–∑–∞–Ω–Ω–æ–π —Å—Å—ã–ª–∫–µ
    browser.get(link)

    # –Ω–∞—Ö–æ–¥–∏–º —ç–ª–µ–º–µ–Ω—Ç, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π 'x'
    x_element = browser.find_element(By.XPATH, '//span[@id="input_value"]')
    print(x_element)

    # –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é 'x' —Ç–µ–∫—Å—Ç –∏–∑ —ç–ª–µ–º–µ–Ω—Ç–∞ x_element
    x = x_element.text
    print("x= ", x)

    # –∑–∞–ø–∏—Å—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤—ã—á–∏—Å–ª–µ–Ω–∏—è 'x'-–∞ –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é 'y'
    y = calc(x)
    print("y= ", y)

    # –≤–≤–æ–¥–∏–º –∑–Ω–∞—á–µ–Ω–∏–µ 'y' –≤ —Ç–µ–∫—Å—Ç–æ–≤–æ–µ –ø–æ–ª–µ
    input1 = browser.find_element(By.XPATH, '//input[@id="answer" and @class="form-control" and @required]')
    input1.send_keys(y)

    # –æ—Ç–º–µ—á–∞–µ–º checkbox "I'm the robot":
    option_checkbox = browser.find_element(By.XPATH, '//input[@id="robotCheckbox" and @class="form-check-input" and @required]')
    option_checkbox.click()

    # –≤—ã–±–∏—Ä–∞–µ–º radiobutton "Robots rule!":
    option_radiobutton = browser.find_element(By.XPATH, '//label[@class="form-check-label" and text()="Robots rule"]')
    option_radiobutton.click()

    # –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø–æ–ª–Ω–µ–Ω–Ω—É—é —Ñ–æ—Ä–º—É
    button = browser.find_element(By.XPATH, '//button[@class="btn btn-default"  and text()="Submit"]')
    button.click()

finally:
    # –æ–∂–∏–¥–∞–Ω–∏–µ —á—Ç–æ–±—ã –≤–∏–∑—É–∞–ª—å–Ω–æ –æ—Ü–µ–Ω–∏—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø—Ä–æ—Ö–æ–∂–¥–µ–Ω–∏—è —Å–∫—Ä–∏–ø—Ç–∞
    time.sleep(15)
    # –∑–∞–∫—Ä—ã–≤–∞–µ–º –±—Ä–∞—É–∑–µ—Ä –ø–æ—Å–ª–µ –≤—Å–µ—Ö –º–∞–Ω–∏–ø—É–ª—è—Ü–∏–π
